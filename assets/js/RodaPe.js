/*REDESOCIAL------------REDESOCIAL---------------REDESOCIAL---------------REDESOCIAL---------------REDESOCIAL---------------REDESOCIAL*/
var discordVal = window.document.querySelector('img#discord')
discordVal.addEventListener('mouseover', discordHover)
discordVal.addEventListener('mouseout', discordOut)

function discordHover() {
    discordVal.style.animation = 'redeSocialAnimacaoHover 0.1s 0ms forwards'
    discordVal.style.boxShadow = '3px 3px 10px 0px black'
}
function discordOut() {
    discordVal.style.animation = 'redeSocialAnimacaoOut 0.1s 0ms forwards'
    discordVal.style.boxShadow = 'none'
}
